package resService;

public class Movie {

	public String mName;
	public String[] mTheater;
	public String[] mStartDay;
	public String[] mSeat;
	public int mPrice;
	public int mAge;
	public String mDirector;
	public String[] mActor;
	public String[] mTimeTable;
	public String[] mEndDay;
	public int[] mRating;

	public String getmName() {
		return mName;
	}

	public void setmName(String mName) {
		this.mName = mName;
	}

	public String[] getmTheater() {
		return mTheater;
	}

	public void setmTheater(String[] mTheater) {
		this.mTheater = mTheater;
	}

	public String[] getmStartDay() {
		return mStartDay;
	}

	public void setmStartDay(String[] mStartDay) {
		this.mStartDay = mStartDay;
	}

	public String[] getmSeat() {
		return mSeat;
	}

	public void setmSeat(String[] mSeat) {
		this.mSeat = mSeat;
	}

	public int getmPrice() {
		return mPrice;
	}

	public void setmPrice(int mPrice) {
		this.mPrice = mPrice;
	}

	public int getmAge() {
		return mAge;
	}

	public void setmAge(int mAge) {
		this.mAge = mAge;
	}

	public String getmDirector() {
		return mDirector;
	}

	public void setmDirector(String mDirector) {
		this.mDirector = mDirector;
	}

	public String[] getmActor() {
		return mActor;
	}

	public void setmActor(String[] mActor) {
		this.mActor = mActor;
	}

	public String[] getmTimeTable() {
		return mTimeTable;
	}

	public void setmTimeTable(String[] mTimeTable) {
		this.mTimeTable = mTimeTable;
	}

	public String[] getmEndDay() {
		return mEndDay;
	}

	public void setmEndDay(String[] mEndDay) {
		this.mEndDay = mEndDay;
	}

	public int[] getmRating() {
		return mRating;
	}

	public void setmRating(int[] mRating) {
		this.mRating = mRating;
	}

}
